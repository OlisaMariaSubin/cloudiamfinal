import subprocess, time

REGION = "ap-south-1"

def run(cmd, profile, description=""):
    full_cmd = f"aws {cmd} --profile {profile} --region {REGION}"
    print(f"\n[{profile.upper()}] {description}")
    result = subprocess.run(full_cmd, shell=True, capture_output=True, text=True)
    if result.returncode == 0:
        print("  OK Success")
    else:
        print(f"  FAILED (Expected): {result.stderr.strip().split(chr(10))[0][:70]}")
    time.sleep(0.5)
    return result

def section(title):
    print(f"\n{'='*60}\n  {title}\n{'='*60}")

